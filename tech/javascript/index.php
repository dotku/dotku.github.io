        <h2>Updated: 12/25/2015</h2>
        <p>最近在研究技术，对 JavaScript 有了更多的了解，硅谷这边的前端已经非常工程化了，通过 NodeJS 迅速崛起了一堆的前端技术，比如类似 GEM 的 NPM 包裹管理器，Bower 前端包裹管理器，一堆的前端框架雨后春笋般崛起，甚至现在成为了找工作的标配。</p>
        <p>必修的前端应用框架有这些:</p>
        <ul>
            <li>AngularJS (2009 年)
                <p>Google 的 AngularJS 成为业界神级标配</p>
            </li>
            <li>Backbone (2014 年)
                <p>CoffeeScript 的开发者 Jeremy Ashkenas 所开发的框架，由于 CoffeeScript (2009 年) 在业内很有知名度，甚至微软都推出了 TypeScript (2012 年) 山寨他们，可见 Jeremy 的魅力。</p>
                <p>Backbone 比较类似 AngularJS，不过我觉得比 AngularJS 比较难懂。</p>
            </li>
            <li>React (2013 年)
                <p>React 是 Facebook 的 Jordan Walke 编写的脚本库，听说具有 Mobile Web 兼得的功能，还没有试过</p>
            </li>
        </ul>
        <p>必修的前端框架</p>
        <ul>
            <li>BootStrap
                <p>BootStrap 是 Twitter 公司开发的前端框架，不仅仅只是前面提到的应用的框架，包含了 CSS 的内容，所以是 JS+CS 的框架 (由于语义化的关系，HTML 无法定制，所以基本上改不了了，虽然 Facebook 之前尝试用什么 FBTAG 之类的，不过似乎被淘汰了...)，实际上也就是 BootCSS + jQuery 的感觉，基本上 BootStrap 最大的贡献就是在 CSS 上，而 JS 部分就是 jQuery 的内容。</p>
            </li>
        </ul>
