  function getLangCode() {
    const urlLang = new URLSearchParams(location.search).get('lang');
    if (urlLang && T[urlLang]) return urlLang;
    const stored = localStorage.getItem('dotku.lang');
    if (stored && T[stored]) return stored;
    const nav = (navigator.language || 'en').toLowerCase();
    if (nav.startsWith('zh-tw') || nav.startsWith('zh-hk')) return 'zh-TW';
    if (nav.startsWith('zh')) return 'zh';
    if (nav.startsWith('es')) return 'es';
    if (nav.startsWith('ko')) return 'ko';
    if (nav.startsWith('ja')) return 'ja';
    return 'en';
  }

  let currentCode = 'en';
  const subscribers = [];

  function applyLang(code) {
    const dict = T[code] || T.en;
    const meta = LANGS.find(l => l.code === code) || LANGS[0];
    currentCode = code;
    document.documentElement.lang = meta.htmlLang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (!(key in dict)) return;
      const attr = el.getAttribute('data-i18n-attr');
      if (attr) el.setAttribute(attr, dict[key]);
      else el.textContent = dict[key];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (key in dict) el.innerHTML = dict[key];
    });
    const curLabel = document.getElementById('lang-current');
    if (curLabel) curLabel.textContent = meta.label;
    localStorage.setItem('dotku.lang', code);
    subscribers.forEach(cb => { try { cb(code); } catch (e) { console.error(e); } });
  }

  window.DotkuI18n = {
    t(key, fallback) {
      const d = T[currentCode] || T.en;
      if (key in d) return d[key];
      if (key in T.en) return T.en[key];
      return fallback != null ? fallback : key;
    },
    current() { return currentCode; },
    onChange(cb) { if (typeof cb === 'function') subscribers.push(cb); },
  };

  function renderMenu(current) {
    const menu = document.getElementById('lang-menu');
    if (!menu) return;
    menu.innerHTML = LANGS.map(l => {
      const active = l.code === current;
      return `<li>
        <button type="button" role="option" aria-selected="${active}"
          data-lang="${l.code}"
          class="w-full text-left px-3 py-2 hover:bg-slate-100 flex items-center justify-between ${active ? 'text-brand-700 font-semibold' : 'text-slate-700'}">
          <span>${l.label}</span>
          ${active ? '<span aria-hidden>✓</span>' : ''}
        </button>
      </li>`;
    }).join('');
    menu.querySelectorAll('button[data-lang]').forEach(btn => {
      btn.addEventListener('click', () => {
        const code = btn.getAttribute('data-lang');
        switchLang(code);
      });
    });
  }

  function switchLang(code) {
    if (!T[code]) return;
    const url = new URL(location.href);
    url.searchParams.set('lang', code);
    history.replaceState(null, '', url);
    applyLang(code);
    renderMenu(code);
    closeMenu();
  }

  function openMenu() {
    const menu = document.getElementById('lang-menu');
    const btn = document.getElementById('lang-btn');
    menu.classList.remove('hidden');
    btn.setAttribute('aria-expanded', 'true');
  }
  function closeMenu() {
    const menu = document.getElementById('lang-menu');
    const btn = document.getElementById('lang-btn');
    menu.classList.add('hidden');
    btn.setAttribute('aria-expanded', 'false');
  }
  function toggleMenu() {
    const menu = document.getElementById('lang-menu');
    if (menu.classList.contains('hidden')) openMenu(); else closeMenu();
  }

  document.addEventListener('DOMContentLoaded', () => {
    const code = getLangCode();
    applyLang(code);
    renderMenu(code);
    const btn = document.getElementById('lang-btn');
    if (btn) btn.addEventListener('click', (e) => { e.stopPropagation(); toggleMenu(); });
    document.addEventListener('click', (e) => {
      const menu = document.getElementById('lang-menu');
      if (menu && !menu.contains(e.target) && e.target.id !== 'lang-btn') closeMenu();
    });
  });
